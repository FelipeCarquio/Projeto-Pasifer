function menu() {
    if (links.style.display == 'flex') {
        links.style.display = 'none'
    } else {
        links.style.display = 'flex'
    }
}

function perfil() {
    if (perfilUser.style.display == 'flex') {
        perfilUser.style.display = 'none'
    } else {
        perfilUser.style.display = 'flex'
    }
}